//switch es una forma diferente de validar 
var numbers = 1
switch(numbers){
    case 1:
        console.log("soy un uno");
        break;
    case 2:
        console.log("soy un dos");
        break;
    case 100:
        console.log("si soy un cien")
        break;
    default:
        console.log("no soy nada")
}